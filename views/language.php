        <p>Deep English is founded by Dan and Aaron. Here's what they say about themselves and about their project.<br/></p>
        <p> With Master's degrees in teaching English and online learning, and years of experience teaching English at the 
        university level, we are confident in our abilities to help you take your 
        English speaking and listening to the next level.</p>
        <h4>Real World English</h4>
        <p>Over the years we have seen three main problems for English learners: 
        boring materials, ineffective learning methods, and lack of time to attend classes. 
        We decided to make something different.
        We wanted to create something new and meaningful for people like us. 
        People who love to learn, love to laugh, and are always looking to experience 
        the world with fresh eyes.
        Our mission is to get you passionate about using English to communicate and understand 
        real, meaningful ideas. To do that, we created a different kind of English lesson, built around 
        interesting subjects that make people think deeply.</p>
        <h4>Vision</h4>
        <p>We are here to make a difference. It may sound "corny" but we believe that 
        every person has an opportunity to make the world a better place. 
        Sending ripples of positivity into the world is something we all have a chance
         to do regardless of what kind of job we have. We are English teachers, so we try 
        to do this by making lessons that inspire people to grow, live life more fully, 
        and think about the world in new ways.</p>
        <h4>Mission</h4>
        <p>Our mission is to help you reach your English fluency goals. To do that, 
        we have to give you a shot of raw honesty: Deep English isn't for everyone. 
        We have created our lessons for people who are open-minded and 
        ready to make the jump to English fluency. This isn't for beginners.
        Are Deep English lessons right for you? There is only one way to find out. 
        Over the past 4 years we have created a library of free listening lessons 
        and a free 7-day course teaching our method of how to improve 
        your listening and speaking fluency.<br/></p>
